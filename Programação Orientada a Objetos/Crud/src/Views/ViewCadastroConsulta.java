    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        ConsultaController cc = new ConsultaController();
        
        if(cc.create(Integer.parseInt(txtId.getText()), txtData.getText())){
            JOptionPane.showMessageDialog(rootPane, "Consulta marcada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao marcar consulta!");
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void txtIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyReleased
        if(txtId.getText()==""){
            txtNome.setText("");
        } else {
            ConsultaController cc = new ConsultaController();
            txtNome.setText(cc.findPacienteById(txtId.getText()).get(1));
        }
    }//GEN-LAST:event_txtIdKeyReleased


